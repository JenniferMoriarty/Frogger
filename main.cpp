int main()
{

	for (int i = 0; i < 30; i++) {
		for (int j = 0; j < 90; j++)
			cout << map[i][j];
		cout << endl;
	}


	ALLEGRO_DISPLAY *display = NULL;
	ALLEGRO_EVENT_QUEUE *event_queue = NULL;
	ALLEGRO_TIMER *timer = NULL;
	ALLEGRO_BITMAP *player = NULL;
	int player_x = SCREEN_W / 2.0 - player_SIZE / 2.0;
	int player_y = SCREEN_H - player_SIZE;
	bool key[5] = { false, false, false, false, false };
	bool redraw = true;
	bool doexit = false;
	int offset = 0;
	int lives = 400;
	al_init();
	al_init_font_addon();
	al_init_ttf_addon();
	ALLEGRO_FONT *font = al_load_font("corbel.ttf", 30, NULL);

	al_install_keyboard();
	al_init_primitives_addon();

	enum STATES { INTRO, PLAYING, PAUSE, END };
	int state = INTRO;
	int StateTimer = 0; //lets you automatically move from one state to another
	

	timer = al_create_timer(1.0 / FPS);
	int jumptimer = 0;


	display = al_create_display(SCREEN_W, SCREEN_H);


	player = al_create_bitmap(player_SIZE, player_SIZE);


	al_set_target_bitmap(player);

	al_clear_to_color(al_map_rgb(255, 0, 255));

	al_set_target_bitmap(al_get_backbuffer(display));

	event_queue = al_create_event_queue();

	al_register_event_source(event_queue, al_get_display_event_source(display));

	al_register_event_source(event_queue, al_get_timer_event_source(timer));

	al_register_event_source(event_queue, al_get_keyboard_event_source());

	al_clear_to_color(al_map_rgb(0, 0, 0));

	al_flip_display();

	al_start_timer(timer);

	while (!doexit)
	{
		ALLEGRO_EVENT ev;
		al_wait_for_event(event_queue, &ev);

		if (ev.type == ALLEGRO_EVENT_TIMER) {
				StateTimer++;
				cout << "statetimer is " << StateTimer << endl;
			
			if (state == INTRO) {

				if (StateTimer > 100) {
					state = PLAYING;
					StateTimer = 0;
				}
			}

			if (state == PLAYING) {

				//lose and win condition
				if (lives <= 0 || player_y < 2) {
					StateTimer = 0;
					state = END;
				}

				//jumping timer (don't allow smooth movement)
				jumptimer--;
				if (jumptimer > 10)
					jumptimer = 0;

				//scroll the map
				offset += 5;
				if (offset > SCREEN_W) //this loops the map
					offset = 0;

				if (map[((player_y + 15) / 30)][(player_x + offset) / 30] == 1) {
					cout << "collision with purple";
					player_y = SCREEN_H - player_SIZE;
					lives--;
				}

				if (map[((player_y + 15) / 30)][(player_x - offset) / 30] == 1) {
					cout << "collision with green";
					player_y = SCREEN_H - player_SIZE; //reset position to start
					lives--;
				}

				//movement algorithm
				if (key[KEY_UP] && player_y >= 4.0 && jumptimer < 5) {
					jumptimer = 10;
					player_y -= 30.0;
				}

				if (key[KEY_DOWN] && player_y <= SCREEN_H - player_SIZE - 4.0 && jumptimer < 5) {
					player_y += 30.0;
					jumptimer = 10;
				}

				if (key[KEY_LEFT] && player_x >= 4.0&& jumptimer < 5) {
					player_x -= 30.0;
					jumptimer = 10;
				}

				if (key[KEY_RIGHT] && player_x <= SCREEN_W - player_SIZE - 4.0 && jumptimer < 5) {
					player_x += 30.0;
					jumptimer = 10;
				}
				if (key[KEY_P] && StateTimer >10) {
					state = PAUSE;
					cout << "pausing." << endl;
					StateTimer = 0;
				}
			}

			if (state == PAUSE) {
				if (key[KEY_P] && StateTimer > 10) {
					state = PLAYING;
					StateTimer = 0;
				}

			}
			if (state == END) {
				StateTimer++;
				if (StateTimer > 100)
					doexit = true;
			}

			redraw = true;
		}
		else if (ev.type == ALLEGRO_EVENT_DISPLAY_CLOSE) {
			break;
		}
		else if (ev.type == ALLEGRO_EVENT_KEY_DOWN) {
			switch (ev.keyboard.keycode) {
			case ALLEGRO_KEY_UP:
				key[KEY_UP] = true;
				break;

			case ALLEGRO_KEY_DOWN:
				key[KEY_DOWN] = true;
				break;

			case ALLEGRO_KEY_LEFT:
				key[KEY_LEFT] = true;
				break;

			case ALLEGRO_KEY_RIGHT:
				key[KEY_RIGHT] = true;
				break;
			case ALLEGRO_KEY_P:
				key[KEY_P] = true;
				cout << "p pressed.";
				break;
			}
		}
		else if (ev.type == ALLEGRO_EVENT_KEY_UP) {
			switch (ev.keyboard.keycode) {
			case ALLEGRO_KEY_UP:
				key[KEY_UP] = false;
				break;

			case ALLEGRO_KEY_DOWN:
				key[KEY_DOWN] = false;
				break;

			case ALLEGRO_KEY_LEFT:
				key[KEY_LEFT] = false;
				break;

			case ALLEGRO_KEY_RIGHT:
				key[KEY_RIGHT] = false;
				break;
			case ALLEGRO_KEY_P:
				key[KEY_P] = false;
				break;

			case ALLEGRO_KEY_ESCAPE:
				doexit = true;
				break;
			}
		}

		if (redraw && al_is_event_queue_empty(event_queue)) {
			redraw = false;

			al_clear_to_color(al_map_rgb(0, 0, 0));
			if (state == PLAYING) {
				for (int i = 0; i < 30; i++) {//rows

					if (i % 2 == 0) {
						for (int j = -30; j < 30; j++) {//columns

							if (map[i][j + 30] == 1)//green platforms
								al_draw_filled_rectangle((j * 30) + offset, i * 30, (j * 30) + offset + 30, (i * 30) + 30, al_map_rgb(80, 220, 20));
						}
					}
					else {
						for (int j = 0; j < 90; j++) {//columns
							if (map[i][j] == 1)//purple platforms

								al_draw_filled_rectangle((j * 30) - offset, i * 30, (j * 30) + 30 - offset, (i * 30) + 30, al_map_rgb(80, 20, 120));
						}
					}
				}


				al_draw_textf(font, al_map_rgb(255, 255, 255), 0, 0, NULL, "LIVES:");
				al_draw_textf(font, al_map_rgb(255, 255, 255), 0, 50, NULL, "%d", lives);
				al_draw_bitmap(player, player_x, player_y, 0);
			}

			if (state == INTRO) {
				al_draw_textf(font, al_map_rgb(255, 255, 255), SCREEN_W / 2, SCREEN_H / 2, NULL, "WELCOME TO FROGGER!");
			}
			if (state == PAUSE) {
				al_draw_textf(font, al_map_rgb(255, 255, 255), SCREEN_W / 2, SCREEN_H / 2, NULL, "paused.");
			}
			if (state == END) {
				if(lives<=0)
				al_draw_textf(font, al_map_rgb(255, 255, 255), SCREEN_W/2, SCREEN_H/2, NULL, "you lose");
				else
					al_draw_textf(font, al_map_rgb(255, 255, 255), SCREEN_W / 2, SCREEN_H / 2, NULL, "you win!");
			}


			al_flip_display();

		}
	}

	al_destroy_bitmap(player);
	al_destroy_timer(timer);
	al_destroy_display(display);
	al_destroy_event_queue(event_queue);

	return 0;
}

